export function getToolBySlug(slug) {
  return TOOLS_CONFIG.find(t => t.slug === slug) || null;
}

/** Get all tools in a category */
export function getToolsByCategory(category) {
  return TOOLS_CONFIG.filter(t => t.category === category);
}

/** Get all category slugs */
export function getAllCategories() {
  return [...new Set(TOOLS_CONFIG.map(t => t.category))];
}

/** Get tools related to a given slug */
export function getRelatedTools(slug, limit = 5) {
  const tool = getToolBySlug(slug);
  if (!tool) return [
  ];
  return tool.relatedTools
    .map(s => getToolBySlug(s))
    .filter(Boolean)
    .slice(0, limit);
}
